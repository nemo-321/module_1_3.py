# Создание переменной age и присвоение ей значения
age = 52
# Вывод значения переменной age
print("Age:", age)

# Перезапись age, добавляя новое значение (например, 1)
new_value = 1
age += new_value
# Вывод измененного значения переменной age
print("New Age:", age)

